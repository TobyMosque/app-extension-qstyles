const reset = function () {
  states.forEach(state => {
    Object.keys(state).forEach(key => {
      state[key] = void 0
    })
  })
}

const setTheme = function (name) {
  reset()
  if (themes[name]) {
    themes[name]()
  }
}

const registerTheme = function (name, callback) {
  themes[name] = callback
}

